import request from '../../plugin/axios/axios' // 请求封装

function login() {
    return request({
        method: 'get',
        url: '/login'
    })
}
function loginPro(params) {
    return request({
        method:'get',
        url: '/login',
        data: params
    })
}

const loginService = {
    login,
    loginPro
}

export default loginService
